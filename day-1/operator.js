// --------------- Arithmetic Operator -----------------  //

let a, b, add, sub, mul, div, mod;

a = 10;
b = 20;

add = a + b;
console.log("The result is " + add)
sub = a - b;
console.log("The result is " + sub)
mul = a * b;
console.log("The result is " + mul)
div = a / b;
console.log("The result is " + div)
mod = a % b;
console.log("The result is " + mod)

// Increment
let increment = 5;
increment++
console.log(increment) // will log 6

// Decrement
let decrement = 5;
decrement--
console.log(decrement) // will log 4